class Carrera {

    constructor(nom, depto, fecha, cupo) {
        this.nombre = nom;
        this.departamento = depto;
        this.fecha = fecha;
        this.cupo = cupo;
    }
    toString() {
        return this.nombre + " " + this.departamento + " (" + this.fecha + ")" + this.cupo + "";
    }
}

